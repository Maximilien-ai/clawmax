import { CHANNEL_API_ENDPOINTS } from './channelApi'

const GREEN = '\x1b[32m'
const RED = '\x1b[31m'
const YELLOW = '\x1b[33m'
const RESET = '\x1b[0m'

let testsPassed = 0
let testsFailed = 0

function assert(condition: boolean, message: string) {
  if (!condition) throw new Error(message)
}

function test(name: string, fn: () => void) {
  try {
    fn()
    console.log(`${GREEN}✓${RESET} ${name}`)
    testsPassed++
  } catch (err: any) {
    console.log(`${RED}✗${RESET} ${name}`)
    console.log(`  Error: ${err.message}`)
    testsFailed++
  }
}

console.log(`\n${YELLOW}=== Channel API Helper Test Suite ===${RESET}\n`)

test('groups endpoint uses /api/groups', () => {
  assert(CHANNEL_API_ENDPOINTS.groups === '/api/groups', 'Expected /api/groups')
})

test('communities endpoint uses /api/communities', () => {
  assert(
    CHANNEL_API_ENDPOINTS.communities === '/api/communities',
    'Expected /api/communities',
  )
})

console.log('\n========================================')
console.log(`Tests passed: ${testsPassed}`)
console.log(`Tests failed: ${testsFailed}`)
console.log('========================================\n')

if (testsFailed > 0) {
  console.log(`${RED}Some tests failed${RESET}`)
  process.exit(1)
} else {
  console.log(`${GREEN}All tests passed${RESET}`)
}

